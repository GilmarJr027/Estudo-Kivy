#Definição de lista
lista = ['p','y','t','h','o','n']

#Exibindo primeiro elemento
print(lista[0])

#Exibindo um elemento imicial e um elemento final
print(lista[0:3])

#Exibindo um elemento inicial , final e o passo em que é pulado os elementos 
print(lista[0:5:2])

#Exibindo a nossa lista de forma inversa
print(lista[::-1])



