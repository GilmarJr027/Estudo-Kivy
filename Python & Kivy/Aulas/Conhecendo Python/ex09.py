
"""

9) Faça um algoritmo em que o usuário
informe uma medida em metros.
Em seguida, converta essa medida para
centímetros e envie para a saída padrão:


"""

metros = float(input('Informe qual é a quantidade em metros:'))

centimetros = (metros * 100)

print('A quantidade de {} metros corresponde a {} centimetros'.format(metros, centimetros))




