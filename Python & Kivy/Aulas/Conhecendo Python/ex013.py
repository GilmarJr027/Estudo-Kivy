
"""
12) Faça um algoritmo que solicite a largura
e a altura de um retângulo. Em seguida,
imprima para o usuário quantos metros quadrados possui a área total.
"""
largura = float(input('Informe a Largura do retangulo:'))
altura = float(input('Informe a Altura do retangulo:'))

mt_2 = (largura * altura)


print('Area em metros quadrados desse retangulo é: {}'.format(mt_2))



